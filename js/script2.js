document.addEventListener('DOMContentLoaded', () => {
  const filterButtons = document.querySelectorAll('#project-filters button');
  const statSegments = document.querySelectorAll('.stat-segment');
  const projectCards = document.querySelectorAll('.project-card');

  function updateFilter(filter) {
    filterButtons.forEach(btn => btn.classList.toggle('active', btn.getAttribute('data-filter') === filter));

    statSegments.forEach(segment => {
      segment.classList. remove('active', 'inactive');
      if (filter === 'all') segment.classList.add('active');
      else if (
        (filter === 'python' && segment.classList.contains('stat-python')) ||
        (filter === 'html' && segment.classList.contains('stat-html')) ||
        (filter === 'php' && segment.classList.contains('stat-php')) ||
        (filter === 'js' && segment.classList.contains('stat-js'))
      ) {
        segment.classList.add('active');
      } else {
        segment.classList.add('inactive');
      }
    });

    projectCards.forEach(card => {
      const category = card.getAttribute('data-category');
      card.style.display = (filter === 'all' || category === filter) ? 'block' : 'none';
    });
  }

  filterButtons.forEach(btn => btn.addEventListener('click', () => updateFilter(btn.getAttribute('data-filter'))));
  updateFilter('all');

  const langSelect = document.getElementById("langSelect");
  const savedLang = localStorage.getItem("lang") || "fr";
  if (langSelect) {
    langSelect.value = savedLang;
    langSelect.addEventListener("change", e => {
      const lang = e.target.value;
      localStorage.setItem("lang", lang);
      applyText(lang);
    });
  }
  applyText(savedLang);

  projectCards.forEach(card => card.addEventListener('click', () => openModal(card.dataset.projectId)));

  window.addEventListener('click', e => {
    if (e. target === document.getElementById('projectModal')) closeModal();
  });

  const prevBtn = document.querySelector('.carousel-container .prev');
  const nextBtn = document.querySelector('.carousel-container .next');
  if (prevBtn) prevBtn.addEventListener('click', () => plusSlides(-1));
  if (nextBtn) nextBtn.addEventListener('click', () => plusSlides(1));

  const yearEl = document.getElementById('currentYear');
  if (yearEl) yearEl.textContent = new Date().getFullYear();
});